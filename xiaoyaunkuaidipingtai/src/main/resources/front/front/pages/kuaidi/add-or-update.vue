<script>
    import wPicker from "@/components/w-picker/w-picker.vue";

    export default {
        data() {
            return {
                cross: '',
                ro:{
					yonghuId: false,
					dingdanId: false,
					kuaidiName: false,
					kuaidiUuidNumber: true,
					kuaidiPhoto: false,
