            kuaidiTypesOptions: [],
            kuaidiTypesIndex : 0,
            yonghuOptions: [],//用户
            yonghuIndex : 0,//用户下标
            dingdanOptions: [],//订单
            dingdanIndex : 0,//订单下标

        }
        },
        components: {
            wPicker
        },
        computed: {
            baseUrl() {
                return this.$base.url;
            },
        },
        async onLoad(options) {
            let yonghuParams = {
                page: 1,
                limit: 100,
            }
            let yonghuData = await this.$api.page(`yonghu`, yonghuParams);
            this.yonghuOptions = yonghuData.data.list;
            let dingdanParams = {
                page: 1,
                limit: 100,
            }
            let dingdanData = await this.$api.page(`dingdan`, dingdanParams);
            this.dingdanOptions = dingdanData.data.list;
		/*下拉框*/
			let kuaidiTypesParams = {
                page: 1,
                limit: 100,
                dicCode: 'kuaidi_types',
            }
			let kuaidiTypes = await this.$api.page(`dictionary`, kuaidiTypesParams);
			this.kuaidiTypesOptions = kuaidiTypes.data.list


            // 如果是更新操作
            if (options.id) {
                this.ruleForm.id = options.id;
                // 获取信息
                let res = await this.$api.info(`kuaidi`, this.ruleForm.id);
                this.ruleForm = res.data;
            }
            if(options.kuaidiId){
                this.ruleForm.kuaidiId = options.kuaidiId;
            }
            // 跨表
            // this.styleChange()
        },
        methods: {
            yonghuChange(e) {
                this.yonghuIndex = e.target.value
                this.ruleForm.yonghuName = this.yonghuOptions[this.yonghuIndex].yonghuName
                this.ruleForm.yonghuId = this.yonghuOptions[this.yonghuIndex].id
            },
            dingdanChange(e) {
                this.dingdanIndex = e.target.value
                this.ruleForm.dingdanName = this.dingdanOptions[this.dingdanIndex].dingdanName
                this.ruleForm.dingdanId = this.dingdanOptions[this.dingdanIndex].id
            },
            // 下拉变化
            kuaidiTypesChange(e) {
                this.kuaidiTypesIndex = e.target.value
                this.ruleForm.kuaidiValue = this.kuaidiTypesOptions[this.kuaidiTypesIndex].indexName
                this.ruleForm.kuaidiTypes = this.kuaidiTypesOptions[this.kuaidiTypesIndex].codeIndex
            },
            // styleChange() {
            // 	this.$nextTick(() => {
            // 		// document.querySelectorAll('.app-update-pv .cu-form-group .uni-yaoxianStyle-yaoxianStyle').forEach(el=>{
            // 		//   el.style.backgroundColor = this.addUpdateForm.yaoxianStyle.content.backgroundColor
            // 		// })
            // 	})
            // },
			kuaidiPhotoTap() {
                let _this = this;
                this.$api.upload(function(res) {
                    _this.ruleForm.kuaidiPhoto = 'upload/' + res.file;
                    _this.$forceUpdate();
                    _this.$nextTick(() => {
                        // _this.styleChange()
                    })
                });
            },
			// 日期控件
			insertTimeConfirm(val) {
                this.ruleForm.insertTime = val.result;
                this.$forceUpdate();
            },
			// 日期控件
			createTimeConfirm(val) {
                this.ruleForm.createTime = val.result;
                this.$forceUpdate();
            },

            getUUID() {
                return new Date().getTime();
            },
            async onSubmitTap() {
				if ((!this.ruleForm.kuaidiName)) {
                    this.$utils.msg(`快递名称不能为空`);
                    return
                }
				if ((!this.ruleForm.kuaidiUuidNumber)) {
                    this.$utils.msg(`快递编号不能为空`);
                    return
                }
				if ((!this.ruleForm.kuaidiPhoto)) {
                    this.$utils.msg(`快递照片不能为空`);
                    return
                }
                if (this.ruleForm.id) {
                    await this.$api.update(`kuaidi`, this.ruleForm);
                } else {
                    await this.$api.save(`kuaidi`, this.ruleForm);
                }
                uni.setStorageSync('pingluenStateState', true);
                this.$utils.msgBack('提交成功');
            },
            getDate(type) {
                const date = new Date();
                let year = date.getFullYear();
                let month = date.getMonth() + 1;
                let day = date.getDate();
                if (type === 'start') {
                    year = year - 60;
                } else if (type === 'end') {
                    year = year + 2;
                }
                month = month > 9 ? month : '0' + month;;
                day = day > 9 ? day : '0' + day;
                return `${year}-${month}-${day}`;
            },
            toggleTab(str) {
                this.$refs[str].show();
            }
        }
    }
</script>
