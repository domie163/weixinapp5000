            wPicker
        },
        computed: {
            baseUrl() {
                return this.$base.url;
            },
        },
        async onLoad(options) {
            let kuaidiParams = {
                page: 1,
                limit: 100,
            }
            let kuaidiData = await this.$api.page(`kuaidi`, kuaidiParams);
            this.kuaidiOptions = kuaidiData.data.list;
            let yonghuParams = {
                page: 1,
                limit: 100,
            }
            let yonghuData = await this.$api.page(`yonghu`, yonghuParams);
            this.yonghuOptions = yonghuData.data.list;
		/*下拉框*/
			let jiluTypesParams = {
                page: 1,
                limit: 100,
                dicCode: 'jilu_types',
            }
			let jiluTypes = await this.$api.page(`dictionary`, jiluTypesParams);
			this.jiluTypesOptions = jiluTypes.data.list


            // 如果是更新操作
            if (options.id) {
                this.ruleForm.id = options.id;
                // 获取信息
                let res = await this.$api.info(`jilu`, this.ruleForm.id);
                this.ruleForm = res.data;
            }
            if(options.jiluId){
                this.ruleForm.jiluId = options.jiluId;
            }
            // 跨表
            // this.styleChange()
        },
        methods: {
            kuaidiChange(e) {
                this.kuaidiIndex = e.target.value
                this.ruleForm.kuaidiName = this.kuaidiOptions[this.kuaidiIndex].kuaidiName
                this.ruleForm.kuaidiId = this.kuaidiOptions[this.kuaidiIndex].id
            },
            yonghuChange(e) {
                this.yonghuIndex = e.target.value
                this.ruleForm.yonghuName = this.yonghuOptions[this.yonghuIndex].yonghuName
                this.ruleForm.yonghuId = this.yonghuOptions[this.yonghuIndex].id
            },
            // 下拉变化
            jiluTypesChange(e) {
                this.jiluTypesIndex = e.target.value
                this.ruleForm.jiluValue = this.jiluTypesOptions[this.jiluTypesIndex].indexName
                this.ruleForm.jiluTypes = this.jiluTypesOptions[this.jiluTypesIndex].codeIndex
            },
            // styleChange() {
            // 	this.$nextTick(() => {
